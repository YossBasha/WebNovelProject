function changeLanguage(lang) {
  localStorage.setItem("preferredLang", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });

  // Highlight active language in settings modal
  document.querySelectorAll(".lang-option").forEach((opt) => {
    opt.classList.toggle("active", opt.getAttribute("data-lang") === lang);
  });

  // Re-render data if render functions exist
  if (window.renderAllData) window.renderAllData();
  if (window.renderLibrary) window.renderLibrary();
  if (window.renderAuthors) window.renderAuthors();
  if (window.renderAuthorDetails) window.renderAuthorDetails();
  if (window.renderNovelDetails) window.renderNovelDetails();
  if (window.renderDiscoverPage) window.renderDiscoverPage();
  
  // Custom updates for elements with icons or complex structures
  updateUIExtraneous(lang);

  // Restore username if logged in
  if (typeof checkAuthState === "function") {
    checkAuthState();
  }
}

function updateUIExtraneous(lang) {
    // Handling elements that have icons inside them which textContent would strip
    const contactBtn = document.querySelector('[data-i18n="btn_send"]');
    if(contactBtn) contactBtn.innerHTML = translations[lang].btn_send;
    
    const exploreBtn = document.querySelector('[data-i18n="explore_btn"]');
    if(exploreBtn) exploreBtn.innerHTML = translations[lang].explore_btn;

    const aboutTitle = document.querySelector('[data-i18n="about_title"]');
    if(aboutTitle) aboutTitle.innerHTML = translations[lang].about_title;
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("preferredLang") || "en";
  changeLanguage(savedLang);

  // Delegate clicks for language options in settings modal
  document.addEventListener("click", (e) => {
    const langOpt = e.target.closest(".lang-option");
    if (langOpt) {
      const lang = langOpt.getAttribute("data-lang");
      changeLanguage(lang);
    }
    
    // Genre preference toggle simulation
    const genreOpt = e.target.closest(".genre-option");
    if (genreOpt) {
        genreOpt.classList.toggle("active");
    }
  });
});
